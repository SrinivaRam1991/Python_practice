# Arithmatic Operators

#Addition (+)
x = 5
y = 7
z = x + y 
print('Addition:',z)

# Subtraction (-)
print('Subtraction:', (x - y))

# Multiplication (*)
print('Multiplication:', (x*y))

# Floor Division (//)
print('Floor Division:', (x // y))

# Exponentiation (**)
print('Exponentiation:' ,(x**y))

# Division (/)
print('Division:' ,(x / y))

# Modulus (%)
print('Modulus:', (x % y))